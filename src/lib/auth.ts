import { NextAuthOptions } from "next-auth"
import { PrismaAdapter } from "@next-auth/prisma-adapter"
import GoogleProvider from "next-auth/providers/google"
import CredentialsProvider from "next-auth/providers/credentials"
import { prisma } from "@/lib/prisma"
import { compare } from "bcryptjs"

export const authOptions: NextAuthOptions = {
    adapter: PrismaAdapter(prisma),
    session: {
        strategy: "jwt",
    },
    pages: {
        signIn: "/auth/signin",
    },
    providers: [
        GoogleProvider({
            clientId: process.env.GOOGLE_CLIENT_ID!,
            clientSecret: process.env.GOOGLE_CLIENT_SECRET!,
        }),
        CredentialsProvider({
            name: "Credentials",
            credentials: {
                email: { label: "Email", type: "email" },
                password: { label: "Password", type: "password" }
            },
            async authorize(credentials) {
                if (!credentials?.email || !credentials?.password) {
                    return null
                }

                const user = await prisma.user.findUnique({
                    where: {
                        email: credentials.email
                    },
                    include: {
                        organization: true
                    }
                })

                if (!user || !user.password) {
                    return null
                }

                const isPasswordValid = await compare(credentials.password, user.password)

                if (!isPasswordValid) {
                    return null
                }

                return {
                    id: user.id,
                    email: user.email,
                    name: user.name,
                    role: user.role,
                    organizationId: user.organizationId,
                    organizationType: user.organization?.type,
                    pendingOrganizationId: user.pendingOrganizationId
                }
            }
        })
    ],
    callbacks: {
        async session({ session, token }) {
            return {
                ...session,
                user: {
                    ...session.user,
                    id: token.id,
                    role: token.role,
                    organizationId: token.organizationId,
                    organizationType: token.organizationType,
                    pendingOrganizationId: token.pendingOrganizationId
                }
            }
        },
        async jwt({ token, user, trigger, session }) {
            if (trigger === "update") {
                // Re-fetch user to get latest organization data
                const freshUser = await prisma.user.findUnique({
                    where: { id: token.id as string },
                    include: { organization: true }
                })

                if (freshUser) {
                    return {
                        ...token,
                        organizationId: freshUser.organizationId,
                        role: freshUser.role,
                        organizationType: freshUser.organization?.type,
                        pendingOrganizationId: freshUser.pendingOrganizationId
                    }
                }
            }

            if (user) {
                return {
                    ...token,
                    id: user.id,
                    role: (user as any).role,
                    organizationId: (user as any).organizationId,
                    organizationType: (user as any).organizationType,
                    pendingOrganizationId: (user as any).pendingOrganizationId
                }
            }
            return token
        }
    }
}
